#!/bin/bash

"filename vimftconfig.sh"

#sh script to create filetype plugins for vim

#you can using comment or delete comment to decided use which filetype you want

#plugin for java filetype
source vimftconfig.sh java
#plugin for xhtml filetype
source vimftconfig.sh xhtml
#plugin for xml filetype
source vimftconfig.sh xml
#plugin for markdown filetype
source vimftconfig.sh markdown
#plugin for python filetype
source vimftconfig.sh python

source vimftconfig.sh sh
source vimftconfig.sh vim

